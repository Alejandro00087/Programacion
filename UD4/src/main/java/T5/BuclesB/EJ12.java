package T5.BuclesB;
import java.util.Scanner;

public class EJ12 {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        System.out.println("Introduce N: ");
        int n = sc.nextInt();

        for (int i = 1; i <= n; i++) {
            System.out.printf("%d\t\n", i );
        }

    }
}
